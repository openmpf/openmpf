package org.mitre.mpf.mvc.model;

/**
 * A POJO used to capture the name and description of each pipeline component
 * (i.e., pipeline, task, action, algorithm) so that XStream can output
 * pipeline component collections, so that XStream can render the proper JSON output
 * for REST services
 */
public class PipelineComponentBasicInfo {
    private String name = null;

    private String description = "";

    public PipelineComponentBasicInfo(
            String name,
            String description ) {
        this.name = name;
        this.description = description;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

}
